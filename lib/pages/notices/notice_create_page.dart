import 'package:cloud_firestore/cloud_firestore.dart';
import 'package:firebase_auth/firebase_auth.dart';
import 'package:flutter/material.dart';

class NoticeCreatePage extends StatefulWidget {
  const NoticeCreatePage({super.key});

  @override
  State<NoticeCreatePage> createState() => _NoticeCreatePageState();
}

class _NoticeCreatePageState extends State<NoticeCreatePage> {
  final _titleController = TextEditingController();
  final _contentController = TextEditingController();
  bool _isPinned = false;
  DateTime? _publishAt;

  Future<void> _pickDateTime(BuildContext context) async {
    final date = await showDatePicker(
      context: context,
      initialDate: _publishAt ?? DateTime.now(),
      firstDate: DateTime.now(),
      lastDate: DateTime.now().add(const Duration(days: 365 * 2)),
    );
    if (date == null) return;

    final time = await showTimePicker(
      context: context,
      initialTime: TimeOfDay.fromDateTime(_publishAt ?? DateTime.now()),
    );
    if (time == null) return;

    setState(() {
      _publishAt = DateTime(
        date.year,
        date.month,
        date.day,
        time.hour,
        time.minute,
      );
    });
  }

  Future<void> _saveNotice() async {
    final title = _titleController.text.trim();
    final content = _contentController.text.trim();

    if (title.isEmpty || content.isEmpty) {
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(const SnackBar(content: Text('Ï†úÎ™©Í≥º ÎÇ¥Ïö©ÏùÑ ÏûÖÎ†•Ìï¥Ï£ºÏÑ∏Ïöî.')));
      return;
    }

    final uid = FirebaseAuth.instance.currentUser?.uid;
    if (uid == null) {
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(const SnackBar(content: Text('Î°úÍ∑∏Ïù∏ Ï†ïÎ≥¥Í∞Ä ÏóÜÏäµÎãàÎã§.')));
      return;
    }

    try {
      await FirebaseFirestore.instance.collection('posts').add({
        'title': title,
        'content': content,
        'isPinned': _isPinned,
        'publishAt': _publishAt != null
            ? Timestamp.fromDate(_publishAt!)
            : Timestamp.now(),
        'authorId': uid,
        'category': 'Í≥µÏßÄ',
        'createdAt': Timestamp.now(),
      });

      if (mounted) {
        ScaffoldMessenger.of(
          context,
        ).showSnackBar(const SnackBar(content: Text('‚úÖ Í≥µÏßÄÍ∞Ä Îì±Î°ùÎêòÏóàÏäµÎãàÎã§.')));
        Navigator.pop(context);
      }
    } catch (e) {
      ScaffoldMessenger.of(
        context,
      ).showSnackBar(SnackBar(content: Text('Îì±Î°ù Ïò§Î•ò: $e')));
    }
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: const Text('‚ûï Í≥µÏßÄ Îì±Î°ù'),
        actions: [
          IconButton(icon: const Icon(Icons.save), onPressed: _saveNotice),
        ],
      ),
      body: Padding(
        padding: const EdgeInsets.all(16.0),
        child: SingleChildScrollView(
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              TextField(
                controller: _titleController,
                decoration: const InputDecoration(
                  labelText: 'Ï†úÎ™©',
                  border: OutlineInputBorder(),
                ),
              ),
              const SizedBox(height: 16),
              TextField(
                controller: _contentController,
                maxLines: 6,
                decoration: const InputDecoration(
                  labelText: 'ÎÇ¥Ïö©',
                  border: OutlineInputBorder(),
                ),
              ),
              const SizedBox(height: 16),
              SwitchListTile(
                title: const Text('Ìôà ÏÉÅÎã® Í≥†Ï†ï (isPinned)'),
                value: _isPinned,
                onChanged: (val) {
                  setState(() {
                    _isPinned = val;
                  });
                },
              ),
              const SizedBox(height: 16),
              Row(
                children: [
                  const Text('üìÖ ÏòàÏïΩÏãúÍ∞Ñ: '),
                  Expanded(
                    child: Text(
                      _publishAt != null
                          ? _publishAt!.toLocal().toString()
                          : 'Ï¶âÏãú Í≤åÏãú',
                      overflow: TextOverflow.ellipsis,
                    ),
                  ),
                  TextButton(
                    onPressed: () => _pickDateTime(context),
                    child: const Text('Î≥ÄÍ≤Ω'),
                  ),
                ],
              ),
              const SizedBox(height: 24),
              SizedBox(
                width: double.infinity,
                child: ElevatedButton.icon(
                  onPressed: _saveNotice,
                  icon: const Icon(Icons.save),
                  label: const Text('Í≥µÏßÄ Îì±Î°ùÌïòÍ∏∞'),
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }
}
