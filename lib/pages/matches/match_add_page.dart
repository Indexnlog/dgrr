  Future<void> _pickDate(BuildContext context, bool isRegisterStart) async {
    final picked = await showDatePicker(
      context: context,
      initialDate: DateTime.now(),
      firstDate: DateTime(2020),
      lastDate: DateTime(2030),
    );
    if (picked != null) {
      setState(() {
        if (isRegisterStart) {
          _registerStart = picked;
        } else {
          _registerEnd = picked;
        }
      });
    }
  }

